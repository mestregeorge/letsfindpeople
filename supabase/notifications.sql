create or replace function public.list_site_notifications(p_limit integer default 20)
returns table (
  id_notification bigint,
  title text,
  body text,
  cover_url text,
  created_at timestamptz,
  is_read boolean
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id integer;
  v_limit integer := least(greatest(coalesce(p_limit, 20), 1), 50);
begin
  if auth.uid() is null then
    raise exception 'Not authenticated';
  end if;

  select u.id_user
    into v_user_id
  from public.users as u
  where u.supabase_uid = auth.uid()::text
    and coalesce(u.is_deleted, false) = false
    and coalesce(u.is_banned, false) = false
    and (u.suspended_until is null or u.suspended_until <= now())
  limit 1;

  if v_user_id is null then
    raise exception 'User not found or unavailable';
  end if;

  return query
    select
      n.id_notification,
      n.title,
      n.body,
      n.cover_url,
      n.created_at,
      exists (
        select 1
        from public.user_notification_reads as r
        where r.id_notification = n.id_notification
          and r.id_user = v_user_id
      ) as is_read
    from public.site_notifications as n
    where n.active = true
    order by n.created_at desc, n.id_notification desc
    limit v_limit;
end;
$$;

create or replace function public.get_unread_site_notification_count()
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id integer;
  v_count integer;
begin
  if auth.uid() is null then
    return 0;
  end if;

  select u.id_user
    into v_user_id
  from public.users as u
  where u.supabase_uid = auth.uid()::text
    and coalesce(u.is_deleted, false) = false
    and coalesce(u.is_banned, false) = false
    and (u.suspended_until is null or u.suspended_until <= now())
  limit 1;

  if v_user_id is null then
    return 0;
  end if;

  select count(*)::integer
    into v_count
  from public.site_notifications as n
  where n.active = true
    and not exists (
      select 1
      from public.user_notification_reads as r
      where r.id_notification = n.id_notification
        and r.id_user = v_user_id
    );

  return coalesce(v_count, 0);
end;
$$;

create or replace function public.mark_site_notification_read(p_notification_id bigint)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id integer;
begin
  if auth.uid() is null then
    raise exception 'Not authenticated';
  end if;

  select u.id_user
    into v_user_id
  from public.users as u
  where u.supabase_uid = auth.uid()::text
    and coalesce(u.is_deleted, false) = false
    and coalesce(u.is_banned, false) = false
    and (u.suspended_until is null or u.suspended_until <= now())
  limit 1;

  if v_user_id is null then
    raise exception 'User not found or unavailable';
  end if;

  if not exists (
    select 1
    from public.site_notifications as n
    where n.id_notification = p_notification_id
      and n.active = true
  ) then
    raise exception 'Notification not found';
  end if;

  insert into public.user_notification_reads (id_notification, id_user)
  values (p_notification_id, v_user_id)
  on conflict (id_notification, id_user)
  do update set read_at = excluded.read_at;

  return true;
end;
$$;

create or replace function public.create_site_notification(
  p_title text,
  p_body text,
  p_cover_url text
)
returns table (
  id_notification bigint,
  title text,
  body text,
  cover_url text,
  created_at timestamptz,
  is_read boolean
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_title text := btrim(coalesce(p_title, ''));
  v_body text := btrim(coalesce(p_body, ''));
  v_cover_url text := nullif(btrim(coalesce(p_cover_url, '')), '');
  v_user_id integer;
begin
  if not public.is_current_admin() then
    raise exception 'Admin access required';
  end if;

  if char_length(v_title) = 0 then
    raise exception 'Title is required';
  end if;

  if char_length(v_title) > 120 then
    raise exception 'Title must be 120 characters or fewer';
  end if;

  if char_length(v_body) = 0 then
    raise exception 'Description is required';
  end if;

  if char_length(v_body) > 2000 then
    raise exception 'Description must be 2000 characters or fewer';
  end if;

  if char_length(v_cover_url) > 2048 then
    raise exception 'Cover URL is too long';
  end if;

  select u.id_user
    into v_user_id
  from public.users as u
  where u.supabase_uid = auth.uid()::text
  limit 1;

  return query
    insert into public.site_notifications (title, body, cover_url, created_by)
    values (v_title, v_body, v_cover_url, v_user_id)
    returning
      site_notifications.id_notification,
      site_notifications.title,
      site_notifications.body,
      site_notifications.cover_url,
      site_notifications.created_at,
      false as is_read;
end;
$$;

