create or replace function public.is_current_admin()
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.users as u
    where u.supabase_uid = auth.uid()::text
      and u.id_type = 2
      and coalesce(u.is_deleted, false) = false
      and coalesce(u.is_banned, false) = false
      and (u.suspended_until is null or u.suspended_until <= now())
  );
$$;

create table if not exists public.site_notifications (
  id_notification bigint generated by default as identity primary key,
  title text not null check (char_length(btrim(title)) between 1 and 120),
  body text not null check (char_length(btrim(body)) between 1 and 2000),
  cover_url text check (cover_url is null or char_length(btrim(cover_url)) between 1 and 2048),
  created_by integer references public.users(id_user) on delete set null,
  active boolean not null default true,
  created_at timestamptz not null default now()
);

alter table public.site_notifications
  alter column cover_url drop not null;

create index if not exists site_notifications_active_created_at_idx
  on public.site_notifications (active, created_at desc);

create table if not exists public.user_notification_reads (
  id_notification bigint not null references public.site_notifications(id_notification) on delete cascade,
  id_user integer not null references public.users(id_user) on delete cascade,
  read_at timestamptz not null default now(),
  primary key (id_notification, id_user)
);

create index if not exists user_notification_reads_id_user_idx
  on public.user_notification_reads (id_user);

alter table public.site_notifications enable row level security;
alter table public.user_notification_reads enable row level security;

drop policy if exists "Authenticated users can read active site notifications"
  on public.site_notifications;

create policy "Authenticated users can read active site notifications"
  on public.site_notifications
  for select
  to authenticated
  using (active = true);

drop policy if exists "Admins can insert site notifications"
  on public.site_notifications;

create policy "Admins can insert site notifications"
  on public.site_notifications
  for insert
  to authenticated
  with check (public.is_current_admin());

drop policy if exists "Users can read their notification read rows"
  on public.user_notification_reads;

create policy "Users can read their notification read rows"
  on public.user_notification_reads
  for select
  to authenticated
  using (
    exists (
      select 1
      from public.users as u
      where u.id_user = user_notification_reads.id_user
        and u.supabase_uid = auth.uid()::text
    )
  );

drop policy if exists "Users can insert their notification read rows"
  on public.user_notification_reads;

create policy "Users can insert their notification read rows"
  on public.user_notification_reads
  for insert
  to authenticated
  with check (
    exists (
      select 1
      from public.users as u
      where u.id_user = user_notification_reads.id_user
        and u.supabase_uid = auth.uid()::text
    )
  );

